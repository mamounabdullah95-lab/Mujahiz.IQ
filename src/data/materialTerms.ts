export function mergeMaterialTerms(customTerms: MaterialTerm[]) {
  const byId = new Map(defaultMaterialTerms.map((term) => [term.id, term]));
  customTerms.forEach((term) => {
    if (term.status === "active") {
      byId.set(term.id, term);
    }
  });
  return Array.from(byId.values());
}
